const scores = {
  ")": 3,
  "]": 57,
  "}": 1197,
  ">": 25137,
};
const pairs = [
  ["{", "}"],
  ["[", "]"],
  ["(", ")"],
  ["<", ">"],
];
const openers = pairs.map((pair) => pair[0]);
const closers = pairs.map((pair) => pair[1]);
const lines10 = data10.split("\n").map((line) => line.split(""));
const corrupteds = lines10.map((line) => {
  let opens: string[] = [];
  return line.find((char) => {
    if (openers.includes(char)) {
      opens.push(char);
    } else if (closers.includes(char)) {
      const opener = opens.pop();
      if (!pairs.find((pair) => pair[0] === opener && pair[1] === char)) {
        return char;
      }
    }
    return false;
  });
});
const part1Score = corrupteds
  .filter(Boolean)
  .map((c) => scores[(c as keyof typeof scores)!])
  .reduce((a, b) => a + b, 0);
console.log("Part 1 score", part1Score);
const incompletes = lines10.filter(
  (_, index) => corrupteds[index] === undefined
);
const addScores = {
  ")": 1,
  "]": 2,
  "}": 3,
  ">": 4,
};
const incompleteScores = incompletes
  .map((line) => {
    let opens: string[] = [];
    line.forEach((char) => {
      if (openers.includes(char)) {
        opens.push(char);
      } else if (closers.includes(char)) {
        const opener = opens.pop();
        if (!pairs.find((pair) => pair[0] === opener && pair[1] === char)) {
          return char;
        }
      }
      return false;
    });
    const closes = opens
      .reverse()
      .map((char) => closers[openers.indexOf(char)]);
    return closes;
  })
  .map((closes) =>
    closes.reduce((a, b) => a * 5 + addScores[b as keyof typeof addScores], 0)
  )
  .sort((a, b) => a - b);

const part2Score = incompleteScores[Math.floor(incompleteScores.length / 2)];

console.log("Part 2 score", part2Score);
