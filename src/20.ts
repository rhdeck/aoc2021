const parseStr20 = (source: string) => {
  const [rulesStr, inputStr] = source.split("\n\n");
  const rules = rulesStr.split("\n").join("").trim().split("");
  const input = inputStr.split("\n").map((r) => r.split(""));
  return { rules, input };
};

const getConvolved = (
  y: number,
  x: number,
  input: string[][],
  useChar = "."
) => {
  //   console.log("Starting with", y, x);
  const convolvedstr = [-1, 0, 1].flatMap((yd) =>
    [-1, 0, 1].map((xd) => {
      const yp = y + yd;
      const xp = x + xd;
      if (xp < 0) return useChar;
      if (yp < 0) return useChar;
      if (yp >= input.length) return useChar;
      if (xp >= input[yp].length) return useChar;
      return input[yp][xp];
    })
  );
  //   console.log(convolvedstr.join(""));
  const binarystr = convolvedstr.map((str) => (str === "#" ? 1 : 0)).join("");
  const i = parseInt(binarystr, 2);
  return i;
};
const frame = (input: string[][], padding: number, useChar = ".") => {
  const out = Array(input.length + padding * 2)
    .fill("!")
    .map((_, i) =>
      Array(input[0].length + padding * 2)
        .fill("!")
        .map((_, j) => {
          const yp = i - padding;
          const xp = j - padding;
          //   console.log("framing pixel", xp, yp); //input[yp][xp]);
          if (xp < 0) return useChar;
          if (yp < 0) return useChar;
          if (yp >= input.length) return useChar;
          if (xp >= input[yp].length) return useChar;
          return input[yp][xp];
        })
    );
  return out;
};

const applyRules = (input: string[][], rules: string[], useChar = ".") => {
  //   console.log("Will apply rules", framedInput);
  const newInput = input.map((row, y) =>
    row.map((_, x) => {
      const i = getConvolved(y, x, input, useChar);
      //   console.log(i);
      const rule = rules[i];
      return rule;
    })
  );
  return newInput;
};
const getBrights = (input: string[][]) =>
  input.flat().filter((str) => str === "#").length;
